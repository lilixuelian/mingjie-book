/*
   使用条件运算符替代练习3-6的程序中的if语句，实现同样的功能。 
*/

#include<stdio.h>

int main (void)
{
	int a,b,c,min;

	puts("请输入三个整数。");

	printf("整数1：");	scanf("%d",&a); 
	printf("整数2：");	scanf("%d",&b); 
	printf("整数3：");	scanf("%d",&c); 

	min = (a < b) ? a : b;
	min = (c < min) ? c : min;
	
	printf("最小值是%d。",min);

	return 0;
}
